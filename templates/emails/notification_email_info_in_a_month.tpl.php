== Ενημέρωση περί Ισχύος Κωδικού ==

Σας ενημερώνουμε ότι ο μυστικός κωδικός που χρησιμοποιείτε με τον λογαριασμό
σας για σύνδεση στις υπηρεσίες του Ιδρύματος έχει περιορισμένη ισχύ και θα 
λήξει σε περίπου έναν μήνα από τώρα.

Εάν επιθυμείτε, μπορείτε να αλλάξετε τον κωδικό τώρα, σύμφωνα με τις
ακόλουθες πληροφορίες.



== Προσοχή στην Ασφάλεια ==

Μην κάνετε "κλικ" σε οποιαδήποτε link ισχυρίζονται ότι είναι υπηρεσίες του 
Ιδρύματος. Μην εμπιστεύεστε οποιαδήποτε μηνύματα ηλεκτρονικού ταχυδρομείου.
Επιβεβαιώστε ότι η σελίδα αλλαγής κωδικού είναι η σωστή: Η σύνδεση είναι
ασφαλής (π.χ. μπλε ή πράσινη ένδειξη στη μπάρα διεύθυνσης) και η διεύθυνση
αναγράφει το domain "example.org".


== Εφαρμογή Αλλαγής Κωδικού ==

Τα εργαλεία διαχείρισης κωδικού βρίσκονται στη διεύθυνση:

https:// account.example.org

Γράψτε την παραπάνω διεύθυνση στο πρόγραμμα πλοήγησης που χρησιμοποιείτε, ή απλά
συνδεθείτε σε οποιαδήποτε υπηρεσία του ιδρύματος και επιλέξετε "Εργαλεία Λογαριασμού".

Σάς ενημερώνουμε ότι στην ίδια διεύθυνση μπορείτε να καταχωρήσετε κάποιο
στοιχείο επικοινωνίας (π.χ. κινητό τηλέφωνο ή δευτερεύουσα e-mail διεύθυνση)
που μπορεί να χρησιμοποιηθεί για την ανάκτηση του κωδικού σας, σε περίπτωση
που τον ξεχάσετε.





